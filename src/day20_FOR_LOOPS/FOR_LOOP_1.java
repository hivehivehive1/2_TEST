package day20_FOR_LOOPS;
public class FOR_LOOP_1 {
	public static void main(String[] args) {
		//Using for loop print "Java!" 10 times
		for(int i = 1; i < 10; i++) {
			System.out.println("Java " + i);
		}
		System.out.println();
		//print numbers from 5 to 15 using a FOR loops
		for(int i = 5; i < 15; i++ ) {
			System.out.print(i + ". ");
		}
		System.out.println();
		System.out.println();
		//FOR loop - print 20 - 10
		for(int num = 15; num >= 5; num--) {
			System.out.print(num + ", ");
		}
}}