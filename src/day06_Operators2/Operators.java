package day06_Operators2;

public class Operators {

	public static void main(String[] args) {
		System.out.println("Hello " + 6 / 10); //Hello 0
		System.out.println("Hello " + 6 * 10); //Hello 60
	//	System.out.println("Hello " + 6 - 10); //ERROR
		System.out.println("Hello " + 6 + 10); //Hello 610
		System.out.println(6 + 10 + " Hello"); //16 Hello
		
		
	}

}
